export interface PdfSection {
  title: string;
  content: string;
  subSections?: { heading: string; body: string }[];
}

export interface PdfDocument {
  title: string;
  subtitle: string;
  patientInfo: { label: string; value: string }[];
  date: string;
  sections: PdfSection[];
  redFlags?: string[];
  medications?: string[];
  followUp?: string;
  generatedBy: string;
  hospitalName: string;
  consultant?: string;
}

function renderDivider(): string {
  return '\n' + '─'.repeat(70) + '\n';
}

function renderSection(s: PdfSection, level: number): string {
  const heading = level === 1 ? `# ${s.title}` : level === 2 ? `## ${s.title}` : `### ${s.title}`;
  const body = s.content || '';
  const subSections = (s.subSections || [])
    .map(sub => `\n### ${sub.heading}\n${sub.body}`)
    .join('\n');
  return [heading, '', body, subSections].join('\n');
}

export function renderPdfText(doc: PdfDocument): string {
  const parts: string[] = [
    '='.repeat(70),
    doc.title.toUpperCase(),
    doc.subtitle,
    '='.repeat(70),
    '',
    `Hospital: ${doc.hospitalName}`,
    `Date: ${doc.date}`,
    `Generated: ${doc.generatedBy}`,
    doc.consultant ? `Consultant: ${doc.consultant}` : '',
    '',
    '─'.repeat(70),
    'PATIENT INFORMATION',
    '─'.repeat(70),
    '',
    doc.patientInfo.map(i => `${i.label}: ${i.value}`).join('\n'),
    '',
  ];

  for (const section of doc.sections) {
    parts.push(renderDivider());
    parts.push(renderSection(section, 1));
  }

  if (doc.redFlags && doc.redFlags.length > 0) {
    parts.push(renderDivider());
    parts.push('⚠ RED FLAGS');
    parts.push('');
    parts.push(doc.redFlags.map(r => `⚠ ${r}`).join('\n'));
  }

  if (doc.medications && doc.medications.length > 0) {
    parts.push(renderDivider());
    parts.push('MEDICATIONS');
    parts.push('');
    parts.push(doc.medications.map(m => `- ${m}`).join('\n'));
  }

  if (doc.followUp) {
    parts.push(renderDivider());
    parts.push('FOLLOW-UP PLAN');
    parts.push('');
    parts.push(doc.followUp);
  }

  parts.push('');
  parts.push('='.repeat(70));
  parts.push('* Generated by AMEXAN Clinical Reasoning System — LBO Module *');
  parts.push('* This is a computer-generated clinical document. Verify before use. *');
  parts.push('='.repeat(70));

  return parts.join('\n');
}

