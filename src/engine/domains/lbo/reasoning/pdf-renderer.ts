export function buildClerkingPdf(params: {
  patientName: string; mrn: string; age: number; sex: string; ward: string; bed: string;
  presentingComplaint: string; hpi: string; pmh: string; medications: string; allergies: string;
  examSummary: string; ddx: string; plan: string; consultant: string; hospital: string;
}): PdfDocument {
  return {
    title: 'Clerking Note — Large Bowel Obstruction',
    subtitle: 'Emergency Surgical Admission',
    patientInfo: [
      { label: 'Name', value: params.patientName },
      { label: 'MRN', value: params.mrn },
      { label: 'Age/Sex', value: `${params.age} / ${params.sex}` },
      { label: 'Ward/Bed', value: `${params.ward} / ${params.bed}` },
    ],
    date: new Date().toLocaleDateString('en-GB', { day: 'numeric', month: 'short', year: 'numeric', hour: '2-digit', minute: '2-digit' }),
    sections: [
      { title: 'Presenting Complaint', content: params.presentingComplaint },
      { title: 'History of Presenting Illness', content: params.hpi },
      { title: 'Past Medical History', content: params.pmh },
      { title: 'Medications & Allergies', content: `Medications: ${params.medications}\nAllergies: ${params.allergies}` },
      { title: 'Examination Findings', content: params.examSummary },
      { title: 'Differential Diagnosis', content: params.ddx },
      { title: 'Plan', content: params.plan },
    ],
    generatedBy: 'AMEXAN LBO Module',
    hospitalName: params.hospital,
    consultant: params.consultant,
  };
}

