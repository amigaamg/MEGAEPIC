const ALL_ENCOUNTERS: EncounterType[] = ['outpatient','emergency','inpatient','ward_round','follow_up','procedure','operative_note','icu_review','telemedicine','antenatal','post_op','discharge_summary','referral','mdt_review'];

const FULL_HISTORY_TYPE: EncounterTypeInfo = { type: 'full_history' as any, label: 'Full History', icon: '📋', color: '#6366f1', activeCases: 0 };

function pickEncounters(ids: EncounterType[]): EncounterTypeInfo[] {
  return ids.map(type => ({ type, ...ENCOUNTER_LABELS[type], activeCases: 0 }));
}

function withFullHistory(arr: EncounterTypeInfo[]): EncounterTypeInfo[] {
  return [...arr, FULL_HISTORY_TYPE];
}

