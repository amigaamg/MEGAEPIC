export function getDepartment(key: string): DepartmentInfo | undefined {
  return WORKSPACE_DATA.find(d => d.key === key);
}

export function getUnit(deptKey: string, unitId: string): UnitInfo | undefined {
  return getDepartment(deptKey)?.units.find(u => u.id === unitId);
}

export function getDepartmentTotalActiveCases(): number {
  return WORKSPACE_DATA.reduce((sum, d) => sum + d.activeCases, 0);
}

export function getDepartmentTotalEncountersToday(): number {
  return WORKSPACE_DATA.reduce((sum, d) => sum + d.todayEncounters, 0);
}
