export function getSymptom(symptomId: string): SymptomEntry | undefined {
  return SYMPTOM_LIBRARY[symptomId];
}

export function searchSymptoms(query: string): SymptomEntry[] {
  const q = query.toLowerCase();
  return Object.values(SYMPTOM_LIBRARY).filter(
    s => s.label.toLowerCase().includes(q) || s.aliases.some(a => a.toLowerCase().includes(q))
  );
}

export function getAllSymptoms(): SymptomEntry[] {
  return Object.values(SYMPTOM_LIBRARY);
}

export function getDifferentialsForSymptoms(symptomIds: string[]): Record<string, { diseaseId: string; diseaseName: string; weight: number }[]> {
  const result: Record<string, { diseaseId: string; diseaseName: string; weight: number }[]> = {};
  for (const id of symptomIds) {
    const s = getSymptom(id);
    if (s) result[id] = s.differentials;
  }
  return result;
}

export function aggregateDifferentials(symptomIds: string[]): Map<string, { diseaseName: string; totalWeight: number; matchedSymptoms: string[] }> {
  const aggregated = new Map<string, { diseaseName: string; totalWeight: number; matchedSymptoms: string[] }>();
  for (const sid of symptomIds) {
    const s = getSymptom(sid);
    if (!s) continue;
    for (const d of s.differentials) {
      const existing = aggregated.get(d.diseaseId);
      if (existing) {
        existing.totalWeight += d.weight;
        existing.matchedSymptoms.push(s.label);
      } else {
        aggregated.set(d.diseaseId, { diseaseName: d.diseaseName, totalWeight: d.weight, matchedSymptoms: [s.label] });
      }
    }
  }
  return aggregated;
}

