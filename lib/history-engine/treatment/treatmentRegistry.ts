export function getTreatmentsForDiseases(diseaseIds: string[]): TreatmentRecommendation[] {
  const seen = new Set<string>();
  const staticResults = TREATMENT_RECOMMENDATIONS.filter(tx => {
    if (seen.has(tx.id)) return false;
    const matches = tx.forCondition.some(did => diseaseIds.includes(did));
    if (matches) { seen.add(tx.id); return true; }
    return false;
  });

  // Check which disease IDs have no static results
  const coveredIds = new Set<string>();
  staticResults.forEach(tx => tx.forCondition.forEach(did => coveredIds.add(did)));
  const missingIds = diseaseIds.filter(did => !coveredIds.has(did));

  if (missingIds.length > 0) {
    const fallback = getFallbackTreatments(missingIds);
    return [...staticResults, ...fallback];
  }

  return staticResults;
}

export function getTreatmentByCategory(diseaseIds: string[], category: TreatmentRecommendation['category']): TreatmentRecommendation[] {
  return getTreatmentsForDiseases(diseaseIds).filter(tx => tx.category === category);
}

export default TREATMENT_RECOMMENDATIONS;
