export function getInvestigationsForDiseases(diseaseIds: string[]): InvestigationRecommendation[] {
  const seen = new Set<string>();
  const staticResults = INVESTIGATION_RECOMMENDATIONS.filter(inv => {
    if (seen.has(inv.id)) return false;
    const matches = inv.recommendedFor.some(did => diseaseIds.includes(did));
    if (matches) { seen.add(inv.id); return true; }
    return false;
  });

  // Check which disease IDs have no static results
  const coveredIds = new Set<string>();
  staticResults.forEach(inv => inv.recommendedFor.forEach(did => coveredIds.add(did)));
  const missingIds = diseaseIds.filter(did => !coveredIds.has(did));

  if (missingIds.length > 0) {
    const fallback = getFallbackInvestigations(missingIds);
    return [...staticResults, ...fallback];
  }

  return staticResults;
}

export function getEssentialInvestigations(diseaseIds: string[]): InvestigationRecommendation[] {
  return getInvestigationsForDiseases(diseaseIds).filter(inv => inv.priority === 'essential');
}

export function getAllInvestigationRecommendations(): InvestigationRecommendation[] {
  return INVESTIGATION_RECOMMENDATIONS;
}

export default INVESTIGATION_RECOMMENDATIONS;
